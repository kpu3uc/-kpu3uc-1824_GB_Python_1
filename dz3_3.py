def thesaurus(*names: str):
    """
    функция thesaurus(), принимает в качестве аргументов имена сотрудников
    и возвращает словарь, в котором ключи — первые буквы имён, а значения — списки,
    содержащие имена, начинающиеся с соответствующей буквы.
    Например:
    # >>>  thesaurus("Иван", "Мария", "Петр", "Илья")
    {
        "И": ["Иван", "Илья"],
        "М": ["Мария"], "П": ["Петр"]
    }
    """
    dic = {}
    # print(names)
    for i in names:
        # print(i[0], i)
        # print(dic.setdefault(i[0], []))
        dic.setdefault(i[0], []).append(i)
    return dic


