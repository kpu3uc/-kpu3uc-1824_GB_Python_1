def thesaurus_adv(*names: str):
    """
    функция thesaurus_adv(), принимает в качестве аргументов строки в формате «Имя Фамилия» и возвращает словарь,
    в котором ключи — первые буквы фамилий, а значения — словари, реализованные по схеме функции thesaurus() и
    содержащие записи, в которых фамилия начинается с соответствующей буквы.
    Например:
    >>>thesaurus_adv("Иван Сергеев", "Инна Серова", "Петр Алексеев", "Илья Иванов", "Анна Савельева")
    {
        "А": {
            "П": ["Петр Алексеев"]
        },
        "С": {
            "И": ["Иван Сергеев", "Инна Серова"],
            "А": ["Анна Савельева"]
        }
    }
    """
    dic = {}
    # print(names)
    for i in names:
        f_n, s_n = i.split(" ")
        dic.setdefault(s_n[0], {}).setdefault(i[0], []).append(i)
        # print(dic)
    return dic


